void UHDAbilitySystemComponent::AddCharacterAbilities(const TArray<TSubclassOf<UGameplayAbility>>& StartupAbilities)
{
	for (const TSubclassOf<UGameplayAbility> AbilityClass : StartupAbilities)
	{
		FGameplayAbilitySpec AbilitySpec = FGameplayAbilitySpec(AbilityClass, 1);
		if (const UHDGameplayAbility* HDAbility = Cast<UHDGameplayAbility>(AbilitySpec.Ability))
		{
			AbilitySpec.DynamicAbilityTags.AddTag(HDAbility->StartupInputTag);
			AbilitySpec.DynamicAbilityTags.AddTag(FHDGameplayTags::Get().Abilities_Status_Equipped);
			GiveAbility(AbilitySpec);
		}
		if (const UNewInputAbilityTest* NewInputAbility = Cast<UNewInputAbilityTest>(AbilitySpec.Ability))
		{
			//AbilitySpec.DynamicAbilityTags.AddTag(NewInputAbility->StartupInputTag);
			AbilitySpec.DynamicAbilityTags.AddTag(FHDGameplayTags::Get().Abilities_Status_Equipped);
			GiveAbility(AbilitySpec);
		}
	}
	bStartupAbilitiesGiven = true;
	AbilitiesGivenDelegate.Broadcast();
}

